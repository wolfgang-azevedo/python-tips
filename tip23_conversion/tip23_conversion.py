### Built-in methods Oct, Bin and Hex, you can convert a integer number to Octal, Binary and Hexadecimal
### Os métodos embarcados Oct and Bin, você converter um número inteiro em Octal, Binário e Hexadecimal

int_to_oct = 12
int_to_bin = 24
int_to_hex = 30

#To convert an Integer to Octal
print(oct(int_to_oct))

#To convert an Integer to Binary
print(bin(int_to_bin))

#To convert an Integer to Hexadecimal
print(hex(int_to_hex))